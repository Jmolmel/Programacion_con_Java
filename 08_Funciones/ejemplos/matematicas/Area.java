package matematicas;

public class Area {
    // Area rombo
    public static double calcularAreaRombo(double diagonalMayor, double diagonalMenor) {
        return (diagonalMayor * diagonalMenor) / 2;
    }

    // Area rectangulo

    public static int calcularAreaRectangulo(int base, int altura) {
        return (base * altura);
    }

}
